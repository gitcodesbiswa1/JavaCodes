package com.asnop;


public class Arithmatic {


	public static void main(String[] args) {
		
		int x=10,y=20;
		int res=x+y;
	    System.out.println("X+Y= "+ res);
	    res=x-y;
	    System.out.println("X-Y= "+ res);
	    res=x*y;
	    System.out.println("X*Y= "+ res);
	    res=x/y;
	    System.out.println("X/Y= "+ res);
	    res=x%3;
	    System.out.println("x%3= "+ res);
	}

}
