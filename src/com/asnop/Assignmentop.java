package com.asnop;


public class Assignmentop {


	public static void main(String[] args) {
		int a=4;
		int var;
		var = a;
		System.out.println("Var using = " + var);
		var+=a;
		System.out.println("Var using += " +var);
		
		var*=a;
		System.out.println("Var using *= " +var );

	}

}
